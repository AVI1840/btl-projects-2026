#!/usr/bin/env node
/**
 * build-internal.js
 * Generates html/index.html from root index.html
 * Adapted for intranet deployment where html/ is the web root.
 *
 * Path changes:
 *   href="html/X.html"       → href="X.html"
 *   href="word/X.docx"       → href="../word/X.docx"
 *   src="html/btl-logo.png"  → src="btl-logo.png"
 *
 * Domain changes:
 *   Live tool buttons default to intranet URLs (snifim.blroot)
 *   Toggle button label flipped (default = internal)
 *
 * Usage: node build-internal.js
 */
const fs = require('fs');
const path = require('path');

const src = path.join(__dirname, 'index.html');
const dest = path.join(__dirname, 'html', 'index.html');

let h = fs.readFileSync(src, 'utf8');

// 1. One-pager links: html/X.html → X.html
h = h.replace(/href="html\//g, 'href="');

// 2. Word downloads: word/ → ../word/
h = h.replace(/href="word\//g, 'href="../word/');

// 3. Logo path: html/btl-logo.png → btl-logo.png
h = h.replace(/src="html\/btl-logo\.png"/g, 'src="btl-logo.png"');
// Also fix the onerror fallback if present
h = h.replace(/onerror="this\.src='btl-logo\.png'"/g, 'onerror="this.src=\'btl-logo.png\'"');

// 4. Default to intranet mode via localStorage preset before main script
const preset = '<script>try{if(!localStorage.getItem("btl-domain-mode"))localStorage.setItem("btl-domain-mode","internal")}catch(e){}</script>';
h = h.replace('</head>', preset + '\n</head>');

// 5. Add generated-file comment
h = '<!-- AUTO-GENERATED by build-internal.js from root index.html — DO NOT EDIT DIRECTLY -->\n' + h;

fs.writeFileSync(dest, h, 'utf8');
console.log('Done: html/index.html (' + h.length + ' chars)');
